      'check': 'Check Payment',
      'bank_transfer': 'Bank Transfer',
      'credit': 'Credit Terms',
    };
    return methods[selectedPaymentMethod] ?? 'Cash Payment';
  }

  String _formatDate(DateTime date) {
    final months = [
      'January',
      'February',
      'March',
      'April',
      'May',
      'June',
      'July',
      'August',
      'September',
      'October',
      'November',
      'December'
    ];
    return '${months[date.month - 1]} ${date.day}, ${date.year}';
  }

