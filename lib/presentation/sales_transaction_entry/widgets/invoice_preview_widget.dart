    return '''
INVOICE

Farm Information:
$farmName
$farmAddress

Invoice Details:
Invoice Number: $invoiceNumber
Date: ${_formatDate(selectedDate)}
Payment Method: $paymentMethodName

Bill To:
${selectedBuyer?['name'] ?? 'N/A'}
${selectedBuyer?['contact'] ?? 'N/A'}
${selectedBuyer?['email'] ?? 'N/A'}

Transaction Details:
Product: Fresh Milk
Quantity: ${quantity.toStringAsFixed(1)} $selectedUnit
Price per ${selectedUnit.substring(0, selectedUnit.length - 1)}: $currencySymbol${pricePerUnit.toStringAsFixed(2)}
Total Amount: $currencySymbol${totalAmount.toStringAsFixed(2)}

Payment Terms: ${selectedBuyer?['paymentTerms'] ?? 'Cash'}

${notes.isNotEmpty ? 'Notes:\n$notes' : ''}

Thank you for your business!

Generated by CowTrack Dairy Management System
''';
  }

  Future<void> _downloadInvoice() async {
    final content = _generateInvoiceContent();
    final filename = 'Invoice_${invoiceNumber.replaceAll('-', '_')}.txt';

    if (kIsWeb) {
      final bytes = utf8.encode(content);
      final blob = html.Blob([bytes]);
      final url = html.Url.createObjectUrlFromBlob(blob);
      final anchor = html.AnchorElement(href: url)
        ..setAttribute("download", filename)
        ..click();
      html.Url.revokeObjectUrl(url);
    } else {
      try {
        final directory = await getApplicationDocumentsDirectory();
        final file = File('${directory.path}/$filename');
        await file.writeAsString(content);
      } catch (e) {
        // Handle error silently
      }
    }
  }

  Future<void> _shareInvoice(BuildContext context) async {
    final content = _generateInvoiceContent();

    if (kIsWeb) {
      // Web sharing via clipboard
      await html.window.navigator.clipboard?.writeText(content);
      if (context.mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          const SnackBar(content: Text('Invoice copied to clipboard')),
        );
      }
    } else {
      // Mobile sharing would use share_plus package in real implementation
      if (context.mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          const SnackBar(
              content: Text('Share functionality available on mobile')),
        );
      }
    }
  }

